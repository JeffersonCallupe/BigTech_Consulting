    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try{
            Producto producto = new Producto();
            if (txtDescripcion.getText().isEmpty() || txtMarca.getText().isEmpty() || txtPrecU.getText().isEmpty()
                 || txtStock.getText().isEmpty() || !lblImage.getText().equals("")){
                producto.setDescripcion(txtDescripcion.getText());
                producto.setMarca(txtMarca.getText());
                producto.setTipo(cboTipo.getSelectedItem().toString());
                producto.setPrecio(Integer.parseInt(txtPrecU.getText()));
                producto.setStock(Integer.parseInt(txtStock.getText()));
                if (guardarImagen() != null){
                    String ruta = guardarImagen().replace("\\", "/").substring(3);
                    producto.setImage(ruta);
                    //jTabbedPane1.setSelectedIndex(4);
                    SqlProductos registro = new SqlProductos();
                    registro.registrar(producto);
                    borrarCampos();
                    JOptionPane.showMessageDialog(null,"Producto registrado correctamente.", "Registrado con éxito.", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null,"Rellene todos los campos.", "ERROR_MESSAGE", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
           JOptionPane.showMessageDialog(null,"Error al registrar producto.\n" + e, "ERROR_MESSAGE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

