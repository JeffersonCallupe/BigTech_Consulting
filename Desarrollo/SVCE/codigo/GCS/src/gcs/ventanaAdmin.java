        private void obtenerDatosVentas(){
            ConexionBD con = new ConexionBD();
        String query = "SELECT c.nombre, c.apellido, p.descripcion, p.marca, v.fecha " +
                "FROM venta v " +
                "JOIN cliente c ON v.id_cliente = c.id_cliente " +
                "JOIN producto p ON v.id_producto = p.id_producto";
        
        try (Connection conn = con.conectar();
             PreparedStatement statement = conn.prepareStatement(query);
             ResultSet resultSet = statement.executeQuery()) {

            while (resultSet.next()) {
                String nombre = resultSet.getString("nombre") + " " + resultSet.getString("apellido");
                String producto = resultSet.getString("descripcion");
                String marca = resultSet.getString("marca");
                String fecha = resultSet.getString("fecha");

                modelo.addRow(new Object[]{nombre, producto, marca, fecha});
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
